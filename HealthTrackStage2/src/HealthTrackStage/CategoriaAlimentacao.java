package HealthTrackStage;

public class CategoriaAlimentacao {
	
	private int codAlimento;
	private String descricao;
	private double caloriaAlimento;
	
	public int getCodAlimento() {
		return codAlimento;
	}
	public void setCodAlimento(int codAlimento) {
		this.codAlimento = codAlimento;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public double getCaloriaAlimento() {
		return caloriaAlimento;
	}
	public void setCaloriaAlimento(double caloriaAlimento) {
		this.caloriaAlimento = caloriaAlimento;
	}
	
	

}
