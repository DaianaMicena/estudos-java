package HealthTrackStage;

public class CategoriaAtividade {
	
	private int codAtividade;
	private String descricao;
	private double caloriaHora;
	private double qtdeHora;
	
	public int getCodAtividade() {
		return codAtividade;
	}
	public void setCodAtividade(int codAtividade) {
		this.codAtividade = codAtividade;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public double getCaloriaHora() {
		return caloriaHora;
	}
	public void setCaloriaHora(double caloriaHora) {
		this.caloriaHora = caloriaHora;
	}
	public double getQtdeHora() {
		return qtdeHora;
	}
	public void setQtdeHora(double qtdeHora) {
		this.qtdeHora = qtdeHora;
	}
	
	

}
